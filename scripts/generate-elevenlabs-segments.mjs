import dotenv from 'dotenv'
import { spawnSync } from 'node:child_process'
import fs from 'node:fs/promises'
import path from 'node:path'

dotenv.config({ path: path.resolve('.env.local'), quiet: true })

const FPS = 30
const apiKey = process.env.ELEVENLABS_API_KEY
const voiceId = process.env.ELEVENLABS_VOICE_ID || 'VywzfvxxNk4yFAaoMm4Q'
const modelIds = (process.env.ELEVENLABS_MODEL_ID || 'eleven_v3,eleven_multilingual_v2')
  .split(',')
  .map((model) => model.trim())
  .filter(Boolean)
const inputPath = process.env.ELEVENLABS_SEGMENTS_PATH || 'public/videos/adiction-boutique-voice-segments.json'
const outputDir = process.env.ELEVENLABS_SEGMENTS_OUTPUT_DIR || 'public/videos/voice-segments'
const timelinePath = process.env.ELEVENLABS_TIMELINE_PATH || 'remotion/generatedVoiceTimeline.ts'
const outputFormat = process.env.ELEVENLABS_OUTPUT_FORMAT || 'mp3_44100_128'

if (!apiKey) {
  throw new Error('Missing ELEVENLABS_API_KEY. Provide it as an env var before running npm run video:elevenlabs:segments.')
}

const segments = JSON.parse(await fs.readFile(inputPath, 'utf8'))
await fs.mkdir(outputDir, { recursive: true })

async function textToSpeech({ text, previousText, nextText }) {
  let lastError = null

  for (const modelId of modelIds) {
    const response = await fetch(
      `https://api.elevenlabs.io/v1/text-to-speech/${voiceId}?output_format=${encodeURIComponent(outputFormat)}`,
      {
        method: 'POST',
        headers: {
          'xi-api-key': apiKey,
          'Content-Type': 'application/json',
          Accept: 'audio/mpeg',
        },
        body: JSON.stringify({
          text,
          model_id: modelId,
          language_code: 'es',
          ...(modelId === 'eleven_v3'
            ? {}
            : {
                previous_text: previousText,
                next_text: nextText,
              }),
          apply_text_normalization: 'on',
          voice_settings: {
            stability: Number(process.env.ELEVENLABS_STABILITY ?? 0.5),
            similarity_boost: Number(process.env.ELEVENLABS_SIMILARITY_BOOST ?? 0.86),
            style: Number(process.env.ELEVENLABS_STYLE ?? 0.28),
            use_speaker_boost: true,
            speed: Number(process.env.ELEVENLABS_SPEED ?? 0.98),
          },
        }),
      },
    )

    if (response.ok) {
      return {
        buffer: Buffer.from(await response.arrayBuffer()),
        modelId,
      }
    }

    lastError = `ElevenLabs TTS failed with ${modelId} (${response.status}): ${await response.text()}`
    console.warn(lastError)
  }

  throw new Error(lastError || 'ElevenLabs TTS failed')
}

async function getAudioDurationInSeconds(filePath) {
  const resolved = JSON.stringify(path.resolve(filePath))
  const result = spawnSync(
    'python',
    ['-c', `from mutagen.mp3 import MP3\nprint(MP3(${resolved}).info.length)`],
    { encoding: 'utf8' },
  )

  if (result.status === 0) {
    return Number(result.stdout.trim())
  }

  const stat = await fs.stat(filePath)
  return (stat.size * 8) / 128000
}

const timeline = []
let cursor = 0
let usedModel = null

for (const [index, segment] of segments.entries()) {
  const filename = `${String(index + 1).padStart(2, '0')}-${segment.id}.mp3`
  const diskPath = path.join(outputDir, filename)
  const publicPath = path.posix.join('videos/voice-segments', filename)
  const previousText = segments[index - 1]?.text
  const nextText = segments[index + 1]?.text

  const { buffer, modelId } = await textToSpeech({
    text: segment.text,
    previousText,
    nextText,
  })

  usedModel = usedModel || modelId
  await fs.writeFile(diskPath, buffer)

  const durationSeconds = await getAudioDurationInSeconds(diskPath)
  const audioDurationFrames = Math.ceil(durationSeconds * FPS)
  const introLeadFrames = segment.id === 'intro' ? 12 : 8
  const tailFrames = segment.id === 'ending' ? 68 : 38
  const durationFrames = Math.max(audioDurationFrames + introLeadFrames + tailFrames, 120)

  timeline.push({
    id: segment.id,
    audioPath: publicPath,
    startFrame: cursor,
    durationFrames,
    audioStartFrame: cursor + introLeadFrames,
    audioDurationFrames,
    durationSeconds: Number(durationSeconds.toFixed(2)),
  })

  cursor += durationFrames
  console.log(`${segment.id}: ${durationSeconds.toFixed(2)}s -> ${durationFrames} frames`)
}

const timelineSource = `// Generated by scripts/generate-elevenlabs-segments.mjs

export const VIDEO_DURATION_IN_FRAMES = ${cursor};

export const voiceTimeline = ${JSON.stringify(timeline, null, 2)} as const;
`

await fs.writeFile(timelinePath, timelineSource)

console.log(`Voice segments generated in: ${outputDir}`)
console.log(`Timeline written: ${timelinePath}`)
console.log(`Model: ${usedModel}`)
console.log(`Video duration: ${(cursor / FPS).toFixed(2)}s`)
